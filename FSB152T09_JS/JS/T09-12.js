// Ejercicio 12: Índices de un array
//Tiene el array: ["lunes", "martes", "miércoles", "jueves", "viernes"]. Utilice for...in para mostrar cada índice y su valor correspondiente.

let arreglo = ["lunes", "martes", "miércoles", "jueves", "viernes"];

for (indice in arreglo){
    console.log(`Indice ${indice}: ${arreglo[indice]}`)
}

