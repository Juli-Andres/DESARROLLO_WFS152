//Ejercicio 06: Área y Perímetro de un Círculo

const radio = 7.5;

const area = Math.PI * (radio * radio);
const perimetro = Math.PI * 2 * radio;


console.log(`Radio: ${radio.toFixed(2)} metros.`);
console.log(`Area: ${area.toFixed(2)} metros cuadrados.`);
console.log(`Perimetro: ${perimetro.toFixed(2)} perimetro.`);

